package com.asiainfo.pc;

public class PrivilegeEngineException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public PrivilegeEngineException() {
		super();
	}

	public PrivilegeEngineException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrivilegeEngineException(String message) {
		super(message);
	}

	public PrivilegeEngineException(Throwable cause) {
		super(cause);
	}
	
	

}
